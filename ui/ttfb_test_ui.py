            proc = subprocess.run(['netsh', 'wlan', 'show', 'interfaces'], 
                                capture_output=True, text=True, timeout=5)
            if 'connected' in proc.stdout.lower():
                wifi_status = 'ok'
                wifi_message = 'WiFi connected'
