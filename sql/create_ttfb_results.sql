CREATE TABLE IF NOT EXISTS ttfb_results (
    id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
    session_id VARCHAR(32) NOT NULL,
    test_start_time DATETIME(6) NOT NULL,
    test_end_time DATETIME(6) NOT NULL,
    `timestamp` DATETIME(6) NOT NULL,
    time_short TIME NOT NULL,
    target_name VARCHAR(255) NOT NULL,
    brand VARCHAR(120) NULL,
    no_internet VARCHAR(120) NULL,
    sample_num SMALLINT UNSIGNED NOT NULL,
    url VARCHAR(2048) NOT NULL,
    ttfb_ms DECIMAL(10,2) NULL,
    lookup_ms DECIMAL(10,2) NULL,
    connect_ms DECIMAL(10,2) NULL,
    total_ms DECIMAL(10,2) NULL,
    http_code SMALLINT UNSIGNED NULL,
    `status` VARCHAR(20) NOT NULL,
    `error` TEXT NULL,
    device_name VARCHAR(255) NULL,
    device_model VARCHAR(255) NULL,
    os_name VARCHAR(100) NULL,
    os_version VARCHAR(100) NULL,
    battery_level TINYINT UNSIGNED NULL,
    battery_charging BOOLEAN NULL,
    wifi_ssid VARCHAR(255) NULL,
    wifi_ssid_method VARCHAR(100) NULL,
    wifi_rssi SMALLINT NULL,
    wifi_band VARCHAR(50) NULL,
    wifi_channel SMALLINT UNSIGNED NULL,
    signal_threshold SMALLINT NULL,
    signal_status VARCHAR(50) NULL,
    dns_primary VARCHAR(255) NULL,
    dns_servers TEXT NULL,
    location_city VARCHAR(120) NULL,
    location_region VARCHAR(120) NULL,
    location_country VARCHAR(120) NULL,
    location_lat DECIMAL(12,9) NULL,
    location_lon DECIMAL(12,9) NULL,
    location_accuracy DECIMAL(10,2) NULL,
    location_altitude DECIMAL(10,2) NULL,
    location_altitude_accuracy DECIMAL(10,2) NULL,
    location_heading DECIMAL(10,2) NULL,
    location_speed DECIMAL(10,2) NULL,
    location_browser_timestamp DATETIME(6) NULL,
    location_saved_at DATETIME(6) NULL,
    location_source VARCHAR(120) NULL,
    location_method VARCHAR(120) NULL,
    location_is_precise BOOLEAN NULL,
    isp VARCHAR(255) NULL,
    public_ip VARCHAR(45) NULL,
    config_ttfb_good_ms INT UNSIGNED NULL,
    config_ttfb_warning_ms INT UNSIGNED NULL,
    config_sample_count INT UNSIGNED NULL,
    config_delay_seconds INT UNSIGNED NULL,
    summary_mean_ttfb DECIMAL(10,2) NULL,
    summary_median_ttfb DECIMAL(10,2) NULL,
    summary_min_ttfb DECIMAL(10,2) NULL,
    summary_max_ttfb DECIMAL(10,2) NULL,
    summary_std_ttfb DECIMAL(10,2) NULL,
    summary_good_count INT UNSIGNED NULL,
    summary_warning_count INT UNSIGNED NULL,
    summary_poor_count INT UNSIGNED NULL,
    summary_total_tests INT UNSIGNED NULL,
    summary_successful_tests INT UNSIGNED NULL,
    summary_failed_tests INT UNSIGNED NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY (id),
    UNIQUE KEY uq_ttfb_results_row (session_id, target_name, sample_num, `timestamp`),
    KEY idx_ttfb_results_session_id (session_id),
    KEY idx_ttfb_results_target_name (target_name),
    KEY idx_ttfb_results_timestamp (`timestamp`),
    KEY idx_ttfb_results_public_ip (public_ip)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;